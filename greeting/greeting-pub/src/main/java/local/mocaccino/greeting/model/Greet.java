package local.mocaccino.greeting.model;

public class Greet {

    private final Long id;
    private final String message;

    public Greet() {
        id = -1L;
        message = "";
    }

    public Greet(Long id, String message) {
        this.id = id;
        this.message = message;
    }

    public long getId() {
        return id;
    }

    public String getMessage() {
        return message;
    }
}
